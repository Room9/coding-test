def binary_search(array, target, start, end):
    while start <= end: #1
        mid = (start + end) // 2 #2
        # 찾은 경우 중간점 인덱스 반환
        if array[mid] == target: #3,4
            return mid
        # 중간점의 값보다 찾고자 하는 값이 작은 경우 왼쪽 확인
        elif array[mid] > target: #3,4
            end = mid - 1
        # 중간점의 값보다 찾고자 하는 값이 작은 경우 오른쪽 확인
        else:
            start = mid + 1 #3,4
    return None #5

